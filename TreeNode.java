
public class TreeNode 
{
	public int value;
	public TreeNode left;
	public TreeNode right;
	
	public TreeNode(int value)
	{
		this.value = value;
		this.left = left;
		this.right = right;
	}
	public int getValue()
	{
		return value;
	}
	public TreeNode getLeft()
	{
		return left;
	}
	public TreeNode getRight()
	{
		return right;
	}
	
}
